//creating class SamAverageMarks to calculate Sam's average marks in Physics, Maths and Chemistry
class SamAverageMarks{
	public static void main(String args[]){
		//Sam's Physics's Marks
		int PhysicsMarks = 95;
		//Sam's Maths Marks 
		int MathsMarks = 94;
		//Sam's Chemistry Marks 
		int ChemistryMarks = 96;
		//Sam's Average Score
		int average = (PhysicsMarks + MathsMarks + ChemistryMarks)/3;
		//Displaying Output
		System.out.print(average);
	}
}
		